}

cmd_cornell() {
  cat <<'EOF'
## 📓 康奈尔笔记法 (Cornell Notes)

请AI将内容整理为康奈尔笔记格式：

### 输入信息
- **主题**: [用户提供]
- **来源**: [课堂/书籍/讲座/视频]
- **原始内容/要点**: [用户提供]

### 康奈尔笔记模板

```markdown
# 📓 康奈尔笔记: [主题]

📅 日期: [日期]
📖 来源: [来源]
🏷️ 标签: #[标签1] #[标签2]

┌─────────────────────────────────────────┐
│  线索栏 (Cue)    │  笔记栏 (Notes)       │
│  (课后填写)       │  (课中记录)            │
├──────────────────┼──────────────────────┤
│                  │                      │
│ ❓ [关键问题1]   │ • [要点1]             │
│                  │   - [细节a]           │
│                  │   - [细节b]           │
│                  │                      │
│ ❓ [关键问题2]   │ • [要点2]             │
│                  │   - [细节a]           │
│                  │   - [例子]            │
│                  │                      │
│ 💡 [关键概念1]   │ • [要点3]             │
│                  │   - [细节a]           │
│                  │   - [公式/定义]       │
│                  │                      │
│ 🔗 [关联知识]    │ • [要点4]             │
│                  │   - [细节]            │
│                  │                      │
├──────────────────┴──────────────────────┤
│  总结 (Summary) — 用自己的话概括         │
│                                          │
│ [2-3句话总结本页核心内容]                │
│ [这部分内容与___的关系是___]             │
│ [我还想了解___]                          │
└─────────────────────────────────────────┘
```

### 使用步骤
1. **课中**: 在笔记栏速记要点
2. **课后24h**: 填写线索栏（关键词/问题）
3. **课后24h**: 写底部总结（不看笔记尝试）
4. **复习时**: 遮住右侧，看线索回忆内容
EOF
}

cmd_zettel() {
  cat <<'EOF'
## 🗃️ 卡片盒笔记 (Zettelkasten)

请AI将内容转化为Zettelkasten卡片：

### 输入信息
- **原始内容**: [用户提供]
- **来源**: [书籍/文章/想法/对话]

### 卡片类型

#### 📖 文献笔记 (Literature Note)
```markdown
---
id: L-[YYYYMMDD]-[序号]
type: literature
source: "[作者]. [书名/文章]. [年份]"
created: [日期]
tags: [#标签1, #标签2]
---

## 原文要点
> "[原文引用]" (p.XX)

## 我的理解
[用自己的话重述，不要抄原文]

## 关联
- 支持: [[卡片ID]] — [为什么关联]
- 反驳: [[卡片ID]] — [为什么矛盾]
- 延伸: [[卡片ID]] — [如何拓展]
```

#### 💡 永久笔记 (Permanent Note)
```markdown
---
id: P-[YYYYMMDD]-[序号]
type: permanent
created: [日期]
tags: [#标签1, #标签2]
links: [相关卡片ID列表]
---

## [一句话标题——概括核心想法]

[用完整的句子表达一个独立想法]
[这个想法应该脱离原文也能理解]

## 论据/支撑
- [来自 L-xxx: ...]
- [来自 L-yyy: ...]
- [个人经验: ...]

## 应用场景
- [何时可以用这个想法]

## 关联卡片
- [[P-xxx]] — [关系说明]
- [[P-yyy]] — [关系说明]
```

#### 📋 项目笔记 (Project Note)
```markdown
---
id: PJ-[项目名]-[序号]
type: project
project: [项目名称]
created: [日期]
status: [进行中/已完成]
---

## 项目: [名称]

### 相关永久笔记
- [[P-xxx]]: [如何应用]
- [[P-yyy]]: [如何应用]

### 待办
- [ ] ___
- [ ] ___

### 进展记录
- [日期]: [进展]
```

### Zettelkasten原则
1. 每张卡片只写**一个想法**
2. 用**自己的话**写，不抄原文
3. 建立**链接**比分类更重要
4. 定期**漫游**卡片发现新连接
5. 从**文献笔记**提炼**永久笔记**
EOF
}

cmd_mindmap() {
  cat <<'EOF'
## 🧠 思维导图 (文字版)

请AI将内容整理为文字版思维导图：

### 输入信息
- **中心主题**: [用户提供]
- **深度**: [2-4层]

### 思维导图模板

```
                    🧠 [中心主题]
                         │
        ┌────────────────┼────────────────┐
        │                │                │
   📌 [分支1]       📌 [分支2]       📌 [分支3]
        │                │                │
   ┌────┼────┐      ┌────┼────┐      ┌────┼────┐
   │    │    │      │    │    │      │    │    │
 [1.1][1.2][1.3] [2.1][2.2][2.3] [3.1][3.2][3.3]
```

### 缩进版（更适合Markdown）
```markdown
# 🧠 [中心主题]

## 📌 [分支1: ___]
  - [子话题1.1]
    - [细节a]
    - [细节b]
  - [子话题1.2]
    - [细节a]
  - [子话题1.3]

## 📌 [分支2: ___]
  - [子话题2.1]
    - [细节a]
    - [细节b]
  - [子话题2.2]
  - [子话题2.3]

## 📌 [分支3: ___]
  - [子话题3.1]
  - [子话题3.2]
  - [子话题3.3]

## 🔗 交叉关联
- [分支1.2] ↔ [分支3.1]: [关系说明]
- [分支2.3] → [分支1.1]: [关系说明]
```

### 思维导图原则
1. 中心主题用**一个词/短语**
2. 一级分支控制在**3-7个**
3. 每个节点用**关键词**（不用完整句子）
4. 加**图标/颜色标签**区分类别
5. 从**整体到细节**展开
EOF
}

