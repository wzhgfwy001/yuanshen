cmd_organize() {
  cat <<'EOF'
## 🗂️ 笔记整理工具

请AI帮助整理和优化现有笔记：

### 输入信息
- **原始笔记**: [用户粘贴]
- **整理目标**: [结构化/精简/扩展/分类/链接]

### 整理服务菜单

#### 1. 结构化整理
将零散笔记整理为清晰结构：
- 提取核心主题
- 按逻辑分组
- 添加层级标题
- 补充过渡语

#### 2. 精简压缩
将冗长笔记精简为要点：
- 删除重复内容
- 提炼关键词
- 缩减为原文1/3
- 保留核心信息

#### 3. 扩展补充
在现有笔记基础上扩展：
- 补充解释
- 添加例子
- 关联知识
- 补充背景

#### 4. 分类打标
为笔记添加分类标签：
```markdown
🏷️ 标签建议:
- 主题: #[主题标签]
- 类型: #笔记 / #想法 / #待办 / #参考
- 状态: #进行中 / #已完成 / #待复习
- 优先级: #重要 / #普通
- 来源: #书籍 / #课程 / #会议 / #灵感
```

#### 5. 链接发现
在笔记之间发现关联：
```
笔记A ↔ 笔记B: [共同主题/互补观点]
笔记C → 笔记D: [因果关系/发展顺序]
笔记E ≠ 笔记F: [矛盾观点/需要调和]
```

### 整理原则
1. **保留原意**: 不改变作者的核心想法
2. **增加可找性**: 让笔记容易被搜索到
3. **建立连接**: 孤立的笔记没有价值
4. **定期复习**: 整理的目的是为了复习
5. **输出导向**: 笔记最终要服务于行动/创作
EOF
}

case "$CMD" in
  cornell)  cmd_cornell ;;
  zettel)   cmd_zettel ;;
  mindmap)  cmd_mindmap ;;
  meeting)  cmd_meeting ;;
